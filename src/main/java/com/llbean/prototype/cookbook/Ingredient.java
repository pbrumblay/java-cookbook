package com.llbean.prototype.cookbook;

public class Ingredient {
	private String name;
	private String amount;
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getName() {
		return this.name;
	}
	
	public void setAmount(String amount) {
		this.amount = amount;
	}
	
	public String getAmount() {
		return this.amount;
	}
}
